package com.leaf.myapp.vo;

public class AdminNoticeVO {
	private int admin_no;
	private String userid;
	private String admin_title;
	private String admin_content;
	private String admin_writedate;
	private int admin_hit;
	private String img;
	private String section;
	
	
	public int getAdmin_no() {
		return admin_no;
	}
	public void setAdmin_no(int admin_no) {
		this.admin_no = admin_no;
	}
	public String getUserid() {
		return userid;
	}
	public void setUserid(String userid) {
		this.userid = userid;
	}
	public String getAdmin_title() {
		return admin_title;
	}
	public void setAdmin_title(String admin_title) {
		this.admin_title = admin_title;
	}
	public String getAdmin_content() {
		return admin_content;
	}
	public void setAdmin_content(String admin_content) {
		this.admin_content = admin_content;
	}
	public String getAdmin_writedate() {
		return admin_writedate;
	}
	public void setAdmin_writedate(String admin_writedate) {
		this.admin_writedate = admin_writedate;
	}
	public int getAdmin_hit() {
		return admin_hit;
	}
	public void setAdmin_hit(int admin_hit) {
		this.admin_hit = admin_hit;
	}
	public String getImg() {
		return img;
	}
	public void setImg(String img) {
		this.img = img;
	}
	public String getSection() {
		return section;
	}
	public void setSection(String section) {
		this.section = section;
	}
	
	
	
}
